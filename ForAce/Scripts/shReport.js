function groupVehicles(shipments) {
  return _.reduce(
    shipments,
    (result, item) => {
      let vehicleNumber = item.fleetInfo.vehicle.vehicleRegistrationNumber;

      if (!result[vehicleNumber]) {
        result[vehicleNumber] = [];
      }

      result[vehicleNumber].push(item);
      return result;
    },
    {}
  );
}
